"""
Exercise: Body Mass Index (IMC) Calculator
Focus: Using variables and math operations to solve a real problem.
"""

name = 'Pedro Henrique'
height = 1.80
weight = 68

# IMC Formula: weight / (height * height)
imc = weight / (height * height)

# Displaying the result
print(name, 'has a height of', height, 'meters,')
print('weighs', weight, 'kg and his BMI (IMC) is:', imc)

# Expected Output:
# Pedro Henrique has a height of 1.80 meters, weighs 68 kg and his BMI is 20.987...