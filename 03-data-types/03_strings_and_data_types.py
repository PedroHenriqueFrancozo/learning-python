print(1234)

# PT-BR: Aspas simples / EN-US: Simple quotes
print('Pedro Henrique')
print(1, 'Pedro Henrique"')

# PT-BR: Aspas duplas / EN-US: Double quotes
print("Pedro Henrique")
print(2, "Pedro Henrique'")

# PT-BR: Escape (\) / EN-US: Escape character (\)
print("Pedro \"Henrique\"")

# PT-BR: Usado para mostrar caracteres de escape (r) / EN-US: Raw string (r) - used to show escape charecters
print(r"Pedro \"Henrique\"")