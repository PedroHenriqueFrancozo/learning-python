# The bool type represents logical answers: Yes (True) or No (False).
# The '==' operator checks if one value is equal to another.

print(10 == 10)  # True (Yes)
print(10 == 11)  # False (No)

# Checking types
print(type(True))       # <class 'bool'>
print(type(10 == 10))   # <class 'bool'>