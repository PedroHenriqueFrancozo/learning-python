# Assignment Operators
# =   Standard assignment
# +=  Addition assignment (counter = counter + 1)
# -=  Subtraction assignment
# *=  Multiplication assignment
# /=  Division assignment
# //= Floor division assignment
# **= Exponentiation assignment
# %=  Remainder (modulo) assignment

counter = 10

# Instead of: counter = counter / 5
counter /= 5

print(f'The result of the assignment division is: {counter}')